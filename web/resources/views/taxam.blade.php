۷.۵ هرگونه امضاء قرارداد بین مشاور معرفی شده توسط تکسام و متقاضیان دریافت مشاوره، تعهدی فی مابین مشاوران و متقاضیان می باشد و تکسام در آن قرارداد هیچ گونه پولی دریافت نمی کند و هیچگونه مسئولیتی ندارد. لذا مشاوران و متقاضیان دریافت مشاوره می بایست کلیه اقدامات قانونی را در قرارداد خود مدنظر داشته باشند. متقاضیان دریافت مشاوره می بایست با مراجعه حضوری به دفتر مشاور و بررسی کلیه اقدامات قانونی، در صورت صلاحدید نسبت به عقد قرارداد با مشاور و واریز وجه اقدام کنند و توجه داشته باشند که تکسام مسئولیتی در قبال قرارداد فی مابین متقاضیان و مشاوران ندارد.</br>
