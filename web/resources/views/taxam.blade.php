۵.۲ کاربران ملزم هستند به محض اینکه هر نوع تغییری در مشخصات و وضعیت شغلی آنها رخ دهد، متناسب با آن پروفایل خود را تغییر دهند. تعلل و سوء استفاده از مشخصات و وضعیت شغلی قبلی، علاوه بر تخلف از قوانین سایت تکسام، ممکن است مصداق جعل عنوان واقع گردد. بدیهی است، تکسام حق پیگیری شکایت از هرگونه جعل را برای خود محفوظ می دارد.</br>
۵.۳ کاربران موظف هستند کلیه قوانین و مقرراتی که در خصوص محتویات قابل انتشار از مراجع قانونی و یا هرگونه اطلاعات محرمانه از جمله اطلاعات کارفرمایان و شرکت های خصوصی صادر گردیده اند را رعایت نمایند. چنانچه محتویاتی خلاف این مقررات توسط کاربران منتشر شود، تکسام مسوولیت سوء استفاده و انتشار این محتویات را نمی پذیرد و به محض دریافت گزارش تخلف از مقررات، محتویات منتشر شده را حذف می نماید.</br>
۵.۴ تکسام محتویات مخاطبان را ذخیره کرده و می تواند به آنها دسترسی داشته باشد و از محتویات بر طبق شرایط حریم خصوصی استفاده نماید.</br>
۵.۵ کلیه کاربران می بایست از درج محتویات غیراخلاقی و غیرقانونی پرهیز نمایند. در صورت گزارش تخلف، تکسام می تواند محتویات را تصحیح، حذف و یا دسترسی به آن را محدود و یا عضویت آن شخص را خاتمه دهد.</br>
۵.۶ کلیه پروفایل ها و محتویات عمومی در تکسام در دسترس همگان قرار دارد و امکان به اشتراک گذاشتن پروفایل و محتویات آن با ذکر منبع از طرق مختلف، از جمله شبکه های اجتماعی و سایت های اشتراک گذاری لینک، توسط مخاطبان وجود دارد.</br>
۵.۷ تکسام مجاز است بر طبق قوانین سایت در این صفحه و موارد مطرح شده در حریم خصوصی از محتویات و پروفایل مخاطبان استفاده نماید؛ از جمله آنها را بصورت برگزیده در قسمت دیگری از سایت نمایش دهد یا در شبکه های اجتماعی دیگر به اشتراک بگذارد.</br>
۵.۸ هرگونه استفاده از تکسام برای فعالیت بازاریابی هرمی ممنوع می باشد.</br>
۵.۹ هرگونه تلاش برای هک و استفاده از حساب کاربری کاربران ممنوع می باشد. همچنین، استخراج انبوه اطلاعات و محتویات مخاطبان (شامل استفاده از ربات ها و کرالر ها، افزونه ها و هر تکنولوژی دیگر و یا به صورت دستی) نیز ممنوع می باشد.</br>
۵.۱۰ مخاطبان می توانند در صورت مشاهده هرگونه تخلف با تکسام تماس بگیرند و تکسام پس از بررسی و صحت گزارش، اقدامات نظارتی و حقوقی لازم را انجام خواهد داد.</br></br>
 
<span style="font-size:20px">۶) سایت های پرسش و پاسخ تکسام</span></br>
۶.۱ کاربران و بازدیدکنندگان می توانند سوالات مدنظر خود در حوزه  مالیات را در سایت پرسش و پاسخ تکسام مطرح کنند.</br>
۶.۲ سوالات و پاسخ های ارائه شده می بایست به زبان فارسی یا انگلیسی باشند. سوالات و پاسخ هایی که به صورت فینگلیش مطرح می شوند حذف می شوند.</br>
۶.۳ سوالات مطرح شده در سایت های پرسش و پاسخ تکسام توسط کاربران تکسام انجام می شود. تکسام هیچ گونه مسئولیتی نسبت به سوالات مطرح شده و پاسخ های ارائه شده ندارد. تکسام صرفا بستر نمایش این سوالات و پاسخ ها را ایجاد می کند. پاسخ های کاربران و مشاوران صرفا مشاوره ای است و مخاطبان تصمیم گیرنده و مسئول اجرای آن پاسخ می باشند. هر شخصی مکلف می باشد شخصا از صحت سوالات مطرح شده و پاسخ های ارائه شده از مراجع ذی صلاح اطمینان حاصل کنند. هر شخصی می تواند در صورت مشاهده هرگونه تخلف با تکسام تماس بگیرند و تکسام پس از بررسی و صحت گزارش، اقدامات نظارتی و حقوقی لازم را انجام خواهد داد.</br>
۶.۴ تکسام هیچ یک از محتویات پروفایل های کاربران، سوالات مطرح شده و پاسخ های ارائه شده را در سایت های پرسش و پاسخ تکسام را تایید نمی کند. هر شخصی مکلف می باشد شخصا از صحت سوالات مطرح شده و پاسخ های ارائه شده از مراجع ذی صلاح اطمینان حاصل کنند. هر شخصی می تواند در صورت مشاهده هرگونه تخلف با تکسام تماس بگیرند و تکسام پس از بررسی و صحت گزارش، اقدامات نظارتی و حقوقی لازم را انجام خواهد داد.</br>
۶.۵ پاسخ های ارائه شده توسط کاربران، براساس اطلاعات ارائه شده در سوالات مطرح شده می باشد و نظر شخصی آنها و نظریه مشاوره ای می باشد. بدیهی است نظر دیگر کارشناسان و مشاوران می تواند متفاوت باشد. نتیجه پرونده نیز می تواند با توجه به شرکت و سازمان مربوطه، آیین نامه های جدید و داخلی و مسئول بررسی پرونده متفاوت باشد. مخاطبان تصمیم گیرنده و مسئول اجرای آن پاسخ می باشند. هر یک از بازدیدکنندگان و کاربران مکلف هستند شخصا از صحت پاسخ های درج شده از مراجع ذی صلاح اطمینان حاصل کنند. بازدیدکنندگان و کاربران می توانند در صورت مشاهده هرگونه پاسخ اشتباه با تکسام تماس بگیرند یا پاسخ را گزارش دهند و تکسام پس از بررسی و صحت گزارش، اقدامات نظارتی لازم را انجام خواهد داد.</br>
۶.۶ ایجاد چند حساب کاربری توسط یک شخص در سایت های پرسش و پاسخ تکسام ممنوع می باشد. تکسام راسا و بدون اخطار قبلی نسبت به حذف کلیه حساب های کاربرانی که توسط یک شخص ایجاد شده است اقدام می کند. بدیهی است، کلیه پاسخ های کاربر خاطی نیز حذف شده و شخصی که حساب کاربری او به این دلیل حذف شده است، حق هیچگونه اعتراضی را ندارد.</br>
۶.۷ آزار و اذیت کاربران در پیام خصوصی و پاسخ های ارائه شده ممنوع می باشد. تکسام راسا و بدون اخطار قبلی نسبت به حذف کاربر خاطی اقدام می کند. بدیهی است، کلیه پاسخ های کاربر خاطی نیز حذف شده و شخصی که حساب کاربری او به این دلیل حذف شده است، حق هیچگونه اعتراضی را ندارد.</br>
۶.۸ هرگونه لینک و هایپرلینک به سایتی غیر از سایت شرکت های بیمه و سازمان های دولتی و حکومتی در سایت های پرسش و پاسخ تکسام، تبلیغات متنی محسوب می شود. همچنین، هایپرلینک در قسمت امضاء به سایت های دیگر تبلیغات متنی محسوب می شود. جهت آگاهی از تعرفه تبلیغات، با ما تماس بگیرید. در صورت مشاهده لینک و هایپرلینک به سایت هایی غیر از سایت شرکت های بیمه و سازمان های دولتی در قسمت پرسش ها و پاسخ ها، لینک یا پاسخ مربوطه حذف و به کاربر خاطی اخطار داده می شود. در صورت تکرار، تکسام راسا و بدون اخطار قبلی نسبت به حذف کاربر خاطی اقدام می کند. بدیهی است، کلیه پاسخ های کاربر خاطی نیز حذف شده و شخصی که حساب کاربری او به این دلیل حذف شده است، حق هیچگونه اعتراضی را ندارد.</br>
۶.۹ کاربران و مشاوران فعال در سایت های پرسش و پاسخ تکسام می بایست به سوالاتی پاسخ دهند که در آن تخصص دارند. در صورت گزارش پاسخی اشتباه، پاسخ غیرمرتبط با حوزه کاری مشاور، پاسخ غیرمرتبط با سوال مطرح شده، پاسخ تخریبی، پاسخ ناکافی/نامفهوم یا پاسخ تبلیغاتی محض، تکسام راسا و بدون اطلاع قبلی اقدام به مسدود سازی حساب کاربری به مدت یک هفته، حذف امضاء و حذف پاسخ های مشمول این بند می کند و این موضوع را به اطلاع کاربر می رساند. مرجع تشخیص اینگونه پاسخ ها تکسام می باشد. در صورت تکرار مجدد پاسخ های مشمول این بند بعد از باز شدن حساب کاربری، حساب کاربری کاربر خاطی به صورت دائم مسدود می شود و شخصی که حساب کاربری او به این دلیل حذف شده است، حق هیچگونه اعتراضی را ندارد.</br>
۶.۱۰ کاربران و مشاوران نمی بایست در قسمت امضاء یا پاسخ خود شماره تماس یا سیستمی اعلام کنند که در صورت تماس کاربران و بازدید کنندگان تکسام امکان کسر هزینه وجود داشته باشد.</br>
۶.۱۱ تکسام مسئولیتی در قبال هرگونه قبول قرارداد توسط کاربران و مشاوران فعال در سایت های پرسش و پاسخ تکسام توسط بازدیدکنندگان را ندارد. پرداخت پول توسط بازدیدکنندگان و کاربران تکسام طبق قرارداد فی مابین بازدیدکنندگان و کاربران تکسام می باشد و تکسام مسئولیتی در این قراردادها ندارد.</br>
۶.۱۲  هرگونه امضاء قرارداد بین مشاوران، کاربران و مخاطبان تعهدی فی مابین آنها می باشد و تکسام در آن قرارداد هیچ گونه پولی دریافت نمی کند و هیچگونه مسئولیتی ندارد. لذا مشاوران، کاربران و مخاطبان می بایست کلیه اقدامات قانونی را در قرارداد خود مدنظر داشته باشند. مخاطبان می بایست با مراجعه حضوری به دفتر مشاور و بررسی کلیه اقدامات قانونی، در صورت صلاحدید نسبت به عقد قرارداد با مشاور و واریز وجه اقدام کنند و توجه داشته باشند که تکسام مسئولیتی در قبال قرارداد فی مابین آنها ندارد.</br>
۶.۱۳ هیچ گونه اطلاعات تماسی در قسمت امضاء، پاسخ، سوال یا دیدگاه نباید قرار بگیرد. در صورت مشاهده، سوال یا پاسخ مربوطه حذف می شود.</br>
۶.۱۴ تبلیغ و ارائه خدمات تشویقی توسط تکسام به مشاور برای فعالیت در سایت های پرسش و پاسخ منوط به ارائه مجوزهای لازم برای ارائه خدمات مشاوره بیمه، مالیات و امور حقوقی می باشد. در صورت عدم ارائه مجوزه های لازم، تبلیغ و خدمات تشویقی به کاربران و مشاوران کان لم یکن خواهد شد.</br></br>

 
<span style="font-size:20px">۷) تکسام – رزرو وقت مشاوره</span></br>
۷.۱ کاربران در تکسام – رزرو وقت مشاوره می توانند نسبت به درخواست مشاوره تلفنی یا حضوری اقدام کنند.</br>
۷.۲ رزرو وقت مشاوره منوط به ارسال پیامک تایید توسط تکسام به کاربر می باشد. پرداخت هزینه مشاوره توسط کاربر شرط کافی برای رزرو جلسه نمی باشد و کاربر می بایست برای تایید وقت مشاوره به پیامک دریافتی تکسام استناد کند.</br>
۷.۳ در صورت عدم رضایت کاربر از مشاوره انجام شده، امکان بازگشت کل هزینه پرداخت شده برای آن مشاوره می باشد. تبصره ۱. هر کاربر فقط یکبار می تواند از امکان برگشت کامل وجه استفاده کند. کاربرانی که یکبار هزینه مشاوره انجام شده به آنها عودت داده شده است، نمی توانند از این امکان مجددا استفاده کنند.</br>
